Inscrição
As inscrições devem ser realizadas por meio depósito ou transferência bancária identificada (no depósito bancário identificado não está autorizada a função do ENVELOPE e nas transferências não utilizar AGENDAMENTOS). O valor da inscrição é de:

R$ 40,00* : até dia 05 de novembro - SEGUNDA-FEIRA (com direito a participação em 1 minicurso);
R$ 50,00* : nas datas do evento.
Aos interessados em participar em mais de um minicurso, o valor adicional de cada minicurso é de:

R$ 15,00*
O comprovante de depósito/transferência, juntamente com a ficha de inscrição impressa, deverão ser entregues na Secretaria do CCT (Vitor ou Cidinha).

Sugerimos consultar na aba de 'Minicursos' as exigências mínimas e a disponibilidade de vagas, bem como os horários de realização dos mesmos.

