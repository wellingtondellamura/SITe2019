Minicursos (em organização)

Os minicursos serão realizados em dias e horários específicos durante a semana do evento. Ocorrendo, eventualmente, nos períodos da tarde e da noite.

As incrições deverão ser efetivadas junto a secretaria do CCT, levando o comprovante de pagamento.

Para participar dos minicursos, obrigatoriamente, deve-se estar inscrito no evento. Ao efetuar a inscrição o participante tem direito a se inscrever em qualquer minicurso. A partir do segundo minicurso tem-se uma taxa adicional de R$ 15,00 por minicurso.

Lembrando, o pagamento deve ser efetuado por meio de depósito bancário identificado.

Instruções para inscrição nos minicursos:

Estar inscrito no evento;
Informar seu nome e o(s) minicurso(s) que deseja participar na secretaria do CCT - UENP (para confirmar sua inscrição);
Pagar a taxa por meio de depósito bancário identificado;
A partir do segundo minicurso, tem-se uma taxa adicional (para cada minicurso) de R$15,00 (verifique se ainda há vagas antes de realizar o pagamento).
