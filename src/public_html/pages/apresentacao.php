<h1>Apresentação</h1>


O SITe (Seminário de Informática e Tecnologia) é um evento tradicional que acontece anualmente no Centro de Ciências Tecnológicas (CCT) da Universidade Estadual do Norte do Paraná (UENP), em Bandeirantes (Campus Luiz Meneghel).

O evento é destinado à comunidade universitária, profissionais das áreas de tecnologia da informação (TI), educação e ciências sociais. Neste ano de 2018, teremos a 11ª edição do evento e acontecerá de 06 a 10 de novembro de 2018.

O XI SITe tem o intuito de proporcionar aos participantes a integração com o mundo acadêmico e com profissionais da área de TI, sendo realizado no decorrer de 5 dias. Durante o evento serão ministrados minicursos e palestras sobre temas que estão em alta no mercado de trabalho e no mundo acadêmico e, neste ano, terá a chamada de trabalhos em formato de resumo expandido (clique aqui), cujos artigos aceitos serão apresentados durante a semana do evento.

Publico Alvo:

O evento é aberto à comunidade, em especial aos estudantes, profissionais da área de informática, pesquisadores, professores, profissionais da área de educação e ciências sociais de instituições de ensino superior.

Objetivos:

Os principais objetivos do XI SITe são:

- Possibilitar aos participantes contato com profissionais que atuam na área de tecnologia da informação, expondo uma perspectiva do mercado de trabalho sob a óptica dos convidados palestrantes;

- Disponibilizar minicursos abordando tópicos que estão na vanguarda da pesquisa e do mercado de trabalho, trazendo as mais recentes tecnologias, tendências, metodologias, ferramentas e produtos disponíveis no mercado de informática, que estejam em evidência no país e no exterior;

- Conhecer e incentivar o desenvolvimento de trabalhos acadêmicos cuja temática esteja focada em TI, possibilitando integração entre os participantes e os pesquisadores.



