<p class="text-justify">
O XII SITe (XII Seminário de Informática e Tecnologia) é um evento anual destinado à comunidade
universitária, profissionais das áreas de tecnologia da informação (TI), educação e ciências sociais. Esta 12a
edição ocorrerá entre os dias 21 e 24 de agosto de 2019, no Centro de Ciências Tecnológicas da Universidade
Estadual do Norte do Paraná (UENP - Campus Luiz Meneghel), em Bandeirantes. O evento tem o intuito de
proporcionar aos participantes a integração com o mundo acadêmico e com profissionais da área de TI, sendo
realizado no decorrer de 4 dias. Em específico para esta edição serão convidados egressos dos cursos de
graduação do CCT para ministrarem as palestras e minicursos. Durante o evento serão ministrados minicursos
e palestras sobre temas que estão em alta no mercado de trabalho e no mundo acadêmico. Terá também
chamada de trabalhos em formato de resumo expandido, cujos artigos aceitos serão apresentados durante a
semana do evento em formato de pôster. Será organizada uma seção especial para a apresentação oral dos
melhores artigos.
</p>


<p class="text-justify">
Os objetivos do XII SITe são:
<ul>
<li>
 Possibilitar aos participantes contato com profissionais, ex-discentes do CCT, os quais atuam na área de
tecnologia da informação, expondo uma perspectiva do mercado de trabalho sob a óptica dos convidados
palestrantes;</li>
<li>Disponibilizar minicursos abordando tópicos que estão na vanguarda da pesquisa e do mercado de
trabalho,trazendo as mais recentes tecnologias, tendências, metodologias, ferramentas e produtos disponíveis
no mercado de informática, que estejam em evidência no país e no exterior;</li>
<li>Conhecer e incentivar o desenvolvimento de trabalhos acadêmicos cuja temática esteja focada em
TI,possibilitando integração entre os participantes e os pesquisadores;</li>
<li>Evidenciar a presença de mulheres na computação.</li>

</ul>
</p>

<p class="text-justify">
A carência de profissionais qualificados na área de TI tem crescido consideravelmente nos últimos anos,
especialmente na região Norte Pioneira do Paraná. Então, o XII SITe visa disponibilizar aos seus participantes
uma visão do mercado de trabalho, enfatizando a necessidade de formação de acadêmicos participativos,
multidisciplinares que busquem constantemente seu crescimento, de seus conhecimentos e habilidades,
destacando qual o perfil profissional que as empresas vem buscando, proporcionando contato entre os
participantes e os palestrantes. Além disso, por meio da submissão e apresentação dos trabalhos científicos,
situar os participantes sobre temas atuais pesquisados e explorados pela literatura, enfatizando quais resultados
vem sendo obtidos nesses trabalhos. Diante deste contexto, o SITe é realizado anualmente, estando em sua 12a
edição no ano de 2019.
</p>

