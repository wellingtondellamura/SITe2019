    <h1>Submissões de Trabalhos</h1>
  </header>
  <main>
  <p class="lead">Cada artigo será submetido à avaliação cega por pares (<em>double-blind reviewing</em>) e julgado segundo sua relevância técnico-científica, adequação ao evento, estrutura, qualidade do texto, resultados apresentados, dentre outros aspectos.</p>

  <p class="lead">Os artigos devem seguir obrigatoriamente o formato definido pela SBC - Sociedade Brasileira de Computação (<a target="_blank" href="http://sbc.org.br/documentos-da-sbc/summary/169-templates-para-artigos-e-capitulos-de-livros/878-modelosparapublicaodeartigos" target="_blank"> <i class="fa fa-download"></i> disponível aqui</a>).</p>

  <h5>Alguns aspectos adicionais devem ser observados:</h5>

  <ol class="lead">
  <li>
  Todos os artigos devem estar no formato Adobe PDF;
  </li>
  <li>
  Deverão ser escritos em português;
  </li>
  <li>
  Os artigos não poderão ultrapassar 4 páginas, incluindo tabelas, figuras e referências.
  </li>
  <li>
  Em nenhuma parte do artigo poderá constar algum tipo de identificação dos autores.
  Os artigos que possuírem identificação serão imediatamente rejeitados.
  </li>
  <li>
  Recomenda-se evitar a menção direta a nomes pessoais (professores, alunos, escolas, municípios, etc.)
  trocando-os por identificadores genéricos (Escola A, Aluno 1, etc.).
  </li>
  <li>
  Na versão final do artigo, caso aceito, os dados dos autores e filiações deverão ser incluídos.
  </li>
  <li>
  Todo e qualquer artigo para o <strong>XII site</strong> deverá ser submetido apenas em formato eletrônico,
  por meio do sistema EasyChair em <a target="_blank" href="https://easychair.org/conferences/?conf=xiisite2019">https://easychair.org/conferences/?conf=xiisite2019</a>.
  </li>
  <li>
  A publicação do artigo implica em todos os seus autores deverão se inscrever no evento.
  </li>
  <li>
  Todos os artigos aceitos deverão ser apresentados em formato de postêr seguindo o modelo (<a target="_blank" href="https://docs.google.com/presentation/d/1ULoWucxG44EzfDyOWkAoR6FfaxwP4MQF6zdcnT8Ec_w/edit?usp=sharing">  <i class="fa fa-download"></i> disponível aqui</a>)
  </li>
  <li>
  Os melhores artigos serão selecionados para apresentação oral em seção especial para esta finalidade.
  </li>
  </ol>

